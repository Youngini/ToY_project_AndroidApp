package com.example.sample01;

public class NoSmokingData {
    private String name;
    private String address;

    public void NoSmokingData(String name, String address){
        this.name = name;
        this.address = address;
    }

    public String getName(){
        return this.name;
    }

    public String getAddress(){
        return this.address;
    }





}